-- Name: user_stats; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.user_stats AS
 SELECT p.id AS user_id,
    p.email,
    p.full_name,
    p.profile_token,
    ( SELECT count(*) AS count
           FROM public.token_activity_logs
          WHERE ((token_activity_logs.profile_id = p.id) AND ((token_activity_logs.log_type)::text = 'profile_token'::text) AND ((token_activity_logs.activity_type)::text = 'viewed'::text))) AS profile_views,
    ( SELECT count(*) AS count
           FROM public.video_tokens
          WHERE (video_tokens.user_id = p.id)) AS total_videos_created,
    ( SELECT count(*) AS count
           FROM public.video_tokens
          WHERE ((video_tokens.user_id = p.id) AND ((video_tokens.status)::text = 'viewed'::text))) AS videos_viewed,
    ( SELECT count(*) AS count
           FROM public.video_tokens
          WHERE ((video_tokens.user_id = p.id) AND ((video_tokens.status)::text = 'active'::text))) AS active_video_tokens,
    ( SELECT count(*) AS count
           FROM public.viewer_responses
          WHERE ((viewer_responses.profile_id = p.id) OR (viewer_responses.video_token_id IN ( SELECT video_tokens.id
                   FROM public.video_tokens
                  WHERE (video_tokens.user_id = p.id))))) AS total_responses,
    ( SELECT count(*) AS count
           FROM public.viewer_responses
          WHERE (((viewer_responses.profile_id = p.id) OR (viewer_responses.video_token_id IN ( SELECT video_tokens.id
                   FROM public.video_tokens
                  WHERE (video_tokens.user_id = p.id)))) AND ((viewer_responses.interest_level)::text = 'interested'::text))) AS interested_responses,
    public.get_response_funnel(p.id) AS response_funnel,
    p.profile_completion_percentage,
    p.profile_completed
   FROM public.profiles p;


